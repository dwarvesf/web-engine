function getIconName(name: string): string {
  const iconPath = icons[name as keyof typeof icons];
  if (!iconPath) {
    const normalizedName = name
      .toLowerCase()
      .replace('icon-', '')
      .replace('i-', '')
      .replace('-icon', '')
      .replace(/-/g, '');
    return normalizedName;
  }
  return name;
}

export default function Icon({ name, size = 'md', className = '' }: IconProps) {
  const normalizedName = getIconName(name);
  const iconPath = icons[normalizedName as keyof typeof icons];

  if (!iconPath) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }

  return (
    <svg
      className={cn(sizes[size], className)}
      fill="none"
      stroke="currentColor"
      viewBox="0 0 24 24"
      xmlns="http://www.w3.org/2000/svg"
    >
      {iconPath}
    </svg>
  );
}
